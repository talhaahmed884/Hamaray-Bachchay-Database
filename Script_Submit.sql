/* 7. LIST OF ALL STUDENTS IN CLASS IN GIVEN TIMESPAN GROUPED BY CLASS  (2020 AS GIVEN TIMESPAN) */
SELECT COUNT(AD.STUDENT_ID) COUNT, SEC.CLASS_ID, SEC.SECTION_ID SECTION, LISTAGG(C.NAME, ', ') WITHIN GROUP (ORDER BY C.NAME) AS STUDENTS
FROM HB_ADMITTED_STUDENT AD, HB_SECTION SEC, HB_REGISTERATION R, HB_CLASS_ASSIGNMENT CA, HB_CHILD C
WHERE AD.STUDENT_ID = R.STUDENT_ID AND CA.REG_ID = R.REG_ID AND SEC.CLASS_ID = CA.CLASS_ID AND SEC.SECTION_ID = CA.SECTION_ID AND C.CHILD_ID = AD.CHILD_ID
AND MONTHS_BETWEEN(TO_DATE('10/10/2020', 'MM/DD/YYYY'), R.REG_DATE) < 12 AND MONTHS_BETWEEN(TO_DATE('10/10/2020', 'MM/DD/YYYY'), AD.JOIN_DATE) < 12
GROUP BY SEC.CLASS_ID, SEC.SECTION_ID;

/* 8. LIST OF ALL NEW PARENTS (DEFAULT 12 MONTHS)(CHANGE NUMBER OF MONTHS RIGHT NEXT TO '<' OPERATORS) */
SELECT AD.STUDENT_ID, C.CHILD, C.DADDY, C.MOMMY,SYSDATE, R.REG_DATE, AD.JOIN_DATE FROM HB_REGISTERATION R, HB_ADMITTED_STUDENT AD, CHILD_INFO C
WHERE MONTHS_BETWEEN(SYSDATE, R.REG_DATE) < 12 AND MONTHS_BETWEEN(SYSDATE, AD.JOIN_DATE) < 12 AND AD.STUDENT_ID = R.STUDENT_ID AND AD.STUDENT_ID = C.STU_ID
ORDER BY AD.STUDENT_ID;

