/* LIST OF ALL STUDENTS NEWLY REGISTERED AND NEWLY ADMITTED */
SELECT AD.STUDENT_ID,SYSDATE, R.REG_DATE, AD.JOIN_DATE FROM HB_REGISTERATION R, HB_ADMITTED_STUDENT AD
WHERE MONTHS_BETWEEN(SYSDATE, R.REG_DATE) < 12 AND MONTHS_BETWEEN(SYSDATE, AD.JOIN_DATE) < 12 AND AD.STUDENT_ID = R.STUDENT_ID;

/* LIST OF ALL STUDENTS NEWLY REGISTERED BUT NOT NEWLY ADMITTED */
SELECT AD.STUDENT_ID,SYSDATE, R.REG_DATE, AD.JOIN_DATE FROM HB_REGISTERATION R, HB_ADMITTED_STUDENT AD
WHERE MONTHS_BETWEEN(SYSDATE, R.REG_DATE) < 12 AND MONTHS_BETWEEN(SYSDATE, AD.JOIN_DATE) > 12 AND AD.STUDENT_ID = R.STUDENT_ID;
