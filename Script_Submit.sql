FROM HB_ADMITTED_STUDENT AD LEFT JOIN HB_REGISTERATION R ON (R.STUDENT_ID = AD.STUDENT_ID)
WHERE R.STUDENT_ID IS NULL AND MONTHS_BETWEEN(SYSDATE, AD.JOIN_DATE) > 12
ORDER BY AD.STUDENT_ID;

