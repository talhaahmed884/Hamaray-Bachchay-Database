SELECT COUNT(AD.STUDENT_ID) COUNT, SEC.CLASS_ID, SEC.SECTION_ID SECTION, LISTAGG(C.NAME, ', ') WITHIN GROUP (ORDER BY C.NAME) AS STUDENTS
FROM HB_ADMITTED_STUDENT AD, HB_SECTION SEC, HB_REGISTERATION R, HB_CLASS_ASSIGNMENT CA, HB_CHILD C
WHERE AD.STUDENT_ID = R.STUDENT_ID AND CA.REG_ID = R.REG_ID AND SEC.CLASS_ID = CA.CLASS_ID AND SEC.SECTION_ID = CA.SECTION_ID AND C.CHILD_ID = AD.CHILD_ID
GROUP BY SEC.CLASS_ID, SEC.SECTION_ID;

/* A VIEW FOR COMPLETE CHILD INFO USING STUDENT ID */
CREATE OR REPLACE VIEW Complete_Child_Info as
SELECT AD.STUDENT_ID STU_ID, AD.CHILD_ID Cid, C.NAME Cname, C.AGE Cage, C.DOB CDoB, C.GENDER gender, C.B_FORM CBform
FROM HB_CHILD C,HB_ADMITTED_STUDENT AD
WHERE C.CHILD_ID = AD.CHILD_ID
ORDER BY AD.STUDENT_ID;

/* A VIEW FOR COMPLETE PARENTS INFO USING STUDENT_ID */
CREATE OR REPLACE VIEW Complete_MOMDAD_Info as
SELECT AD.STUDENT_ID STU_ID, A.PERSON_ID Mid, A.NAME Mname, A.CONTACT Mcontact, A.CNIC Mcnic, A.ADDRESS Maddress, A.EMAIL Memail, A.GENDER
FROM HB_ADULT A,HB_ADMITTED_STUDENT AD, HB_PARENT_INFO PI, CHILD_INFO CI
WHERE PI.PARENT_ID = AD.PARENT_ID AND A.PERSON_ID = PI.PERSON_ID AND CI.stu_id = AD.student_id
ORDER BY AD.STUDENT_ID;

/* A VIEW FOR COMPLETE GUARDIAN INFO USING STUDENT_ID */
CREATE OR REPLACE VIEW Complete_Guard_Info as
SELECT AD.STUDENT_ID STU_ID, G.GUARD_ID Gid,A.PERSON_ID Pid, A.NAME Gname, A.CONTACT Gcontact, A.CNIC Gcnic, A.ADDRESS Gaddress, A.EMAIL Gemail, A.GENDER Ggender, g.relation Grelation
FROM HB_ADULT A,HB_ADMITTED_STUDENT AD, HB_GUARDIAN G, CHILD_INFO CI
WHERE G.GUARD_ID = AD.GUARD_ID AND A.PERSON_ID = G.PERSON_ID AND CI.stu_id = AD.student_id
ORDER BY AD.STUDENT_ID;

