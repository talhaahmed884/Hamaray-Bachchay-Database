/* A VIEW FOR ALL MOTHERS */
CREATE OR REPLACE VIEW MOTHERS AS
SELECT A.NAME, PI.PARENT_ID
FROM HB_ADULT A, HB_PARENT_INFO PI
WHERE PI.PERSON_ID = A.PERSON_ID AND A.GENDER = 'F'
ORDER BY PI.PARENT_ID;

/* A VIEW FOR ALL FATHERS */
CREATE OR REPLACE VIEW FATHERS AS
SELECT A.NAME, PI.PARENT_ID
FROM HB_ADULT A, HB_PARENT_INFO PI
WHERE PI.PERSON_ID = A.PERSON_ID AND A.GENDER = 'M'
ORDER BY PI.PARENT_ID;

/* A VIEW FOR ALL GUARDIANS */
CREATE OR REPLACE VIEW GUARDIANS AS
SELECT A.NAME GUARDIAN, G.GUARD_ID
FROM HB_ADULT A, HB_GUARDIAN G
WHERE G.PERSON_ID = A.PERSON_ID
ORDER BY G.GUARD_ID;

/* A VIEW FOR ALL PARENTS */
CREATE OR REPLACE VIEW PARENTS AS
SELECT DISTINCT PI.PARENT_ID, M.NAME MOMMY, F.NAME DADDY
FROM MOTHERS M, FATHERS F, HB_PARENT_INFO PI
WHERE PI.PARENT_ID = M.PARENT_ID AND PI.PARENT_ID = F.PARENT_ID
ORDER BY PI.PARENT_ID;

/* A VIEW OF CHILD INFO */
CREATE OR REPLACE VIEW CHILD_INFO AS
SELECT AD.STUDENT_ID STU_ID, AD.CHILD_ID CHILD_ID, C.NAME CHILD, P.MOMMY, P.DADDY, G.GUARDIAN
FROM HB_CHILD C, PARENTS P, HB_ADMITTED_STUDENT AD, GUARDIANS G
WHERE AD.PARENT_ID = P.PARENT_ID AND C.CHILD_ID = AD.CHILD_ID AND G.GUARD_ID = AD.GUARD_ID
ORDER BY P.PARENT_ID;

/* A VIEW OF CHILDREN WITH PARENTS WITH LISTAGG*/
CREATE OR REPLACE VIEW CHILDREN_PARENTS AS
SELECT  P.DADDY, P.MOMMY, LISTAGG('(' || AD.STUDENT_ID || ')' || ' ' || COALESCE(C.NAME, ' '), ', ') WITHIN GROUP (ORDER BY C.NAME) AS CHILDREN
FROM HB_CHILD C, PARENTS P, HB_ADMITTED_STUDENT AD
WHERE AD.PARENT_ID = P.PARENT_ID AND C.CHILD_ID = AD.CHILD_ID
GROUP BY P.MOMMY, P.DADDY;

